//Arrays
var listaNomes = ["Fulano", "Ciclano", "Beltrano"]
console.log(listaNomes) //imprimir valores

listaNomes.push('Erculano')
console.log(listaNomes) // imprimir valores

listaNomes.pop()
console.log(listaNomes) //remover o ultimo intem da lista

listaNomes.shift() // remove primeiro item da lista
console.log(listaNomes)

listaNomes.sort() // ordena lista
console.log(listaNomes)

listaNomes.reverse() // inverte a ordem do itens
console.log(listaNomes)
 
console.log('Tamanho da lista ', listaNomes.length)



